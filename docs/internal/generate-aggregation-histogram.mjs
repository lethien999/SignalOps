#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const inputPath = process.argv[2] ?? path.resolve('docs/internal/m11-admin-metrics-after-load.json');
const outputPath = process.argv[3] ?? path.resolve('docs/internal/m11-aggregation-histogram.svg');

const raw = fs.readFileSync(inputPath, 'utf8');
const payload = JSON.parse(raw);
const values = Array.isArray(payload.values) ? payload.values : [];

const buckets = values
  .filter((v) => v.metricName === 'signalops_aggregation_duration_seconds_bucket')
  .map((v) => ({
    le: String(v.labels?.le ?? ''),
    value: Number(v.value ?? 0),
    pipeline: String(v.labels?.pipeline ?? 'unknown')
  }));

if (buckets.length === 0) {
  throw new Error('No histogram bucket values found in input file.');
}

const pipeline = buckets[0].pipeline;
const finiteBuckets = buckets.filter((b) => b.le !== '+Inf');
const maxCount = Math.max(...finiteBuckets.map((b) => b.value), 1);

const width = 1100;
const height = 620;
const margin = { top: 80, right: 40, bottom: 140, left: 90 };
const chartW = width - margin.left - margin.right;
const chartH = height - margin.top - margin.bottom;
const barGap = 10;
const barW = Math.max(18, Math.floor((chartW - barGap * (finiteBuckets.length - 1)) / finiteBuckets.length));

const bars = finiteBuckets
  .map((bucket, i) => {
    const h = Math.round((bucket.value / maxCount) * chartH);
    const x = margin.left + i * (barW + barGap);
    const y = margin.top + (chartH - h);
    const labelX = x + Math.floor(barW / 2);
    return {
      ...bucket,
      x,
      y,
      w: barW,
      h,
      labelX
    };
  });

const yTicks = [0, 0.25, 0.5, 0.75, 1].map((p) => ({
  value: +(maxCount * p).toFixed(2),
  y: margin.top + chartH - Math.round(chartH * p)
}));

const svg = `<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" width="${width}" height="${height}" viewBox="0 0 ${width} ${height}">
  <defs>
    <linearGradient id="bg" x1="0" x2="1" y1="0" y2="1">
      <stop offset="0%" stop-color="#0f172a"/>
      <stop offset="100%" stop-color="#1e293b"/>
    </linearGradient>
    <linearGradient id="bar" x1="0" x2="0" y1="0" y2="1">
      <stop offset="0%" stop-color="#22d3ee"/>
      <stop offset="100%" stop-color="#0891b2"/>
    </linearGradient>
  </defs>

  <rect x="0" y="0" width="${width}" height="${height}" fill="url(#bg)" rx="20"/>

  <text x="${margin.left}" y="42" fill="#e2e8f0" font-size="28" font-family="Segoe UI, Arial, sans-serif" font-weight="700">
    signalops_aggregation_duration_seconds histogram
  </text>
  <text x="${margin.left}" y="68" fill="#94a3b8" font-size="16" font-family="Segoe UI, Arial, sans-serif">
    Pipeline: ${pipeline} | Source: ${path.basename(inputPath)}
  </text>

  ${yTicks
    .map(
      (t) => `<line x1="${margin.left}" y1="${t.y}" x2="${width - margin.right}" y2="${t.y}" stroke="#334155" stroke-width="1" />
  <text x="${margin.left - 12}" y="${t.y + 5}" text-anchor="end" fill="#94a3b8" font-size="12" font-family="Segoe UI, Arial, sans-serif">${t.value}</text>`
    )
    .join('\n  ')}

  <line x1="${margin.left}" y1="${margin.top + chartH}" x2="${width - margin.right}" y2="${margin.top + chartH}" stroke="#cbd5e1" stroke-width="1.5"/>
  <line x1="${margin.left}" y1="${margin.top}" x2="${margin.left}" y2="${margin.top + chartH}" stroke="#cbd5e1" stroke-width="1.5"/>

  ${bars
    .map(
      (b) => `<rect x="${b.x}" y="${b.y}" width="${b.w}" height="${Math.max(1, b.h)}" fill="url(#bar)" rx="6"/>
  <text x="${b.labelX}" y="${margin.top + chartH + 22}" text-anchor="middle" fill="#cbd5e1" font-size="12" font-family="Segoe UI, Arial, sans-serif"><= ${b.le}s</text>
  <text x="${b.labelX}" y="${b.y - 8}" text-anchor="middle" fill="#67e8f9" font-size="12" font-family="Segoe UI, Arial, sans-serif">${b.value}</text>`
    )
    .join('\n  ')}

  <text x="${width - margin.right}" y="${height - 20}" text-anchor="end" fill="#64748b" font-size="12" font-family="Segoe UI, Arial, sans-serif">
    Generated by docs/internal/generate-aggregation-histogram.mjs
  </text>
</svg>
`;

fs.writeFileSync(outputPath, svg, 'utf8');
console.log(`Histogram SVG written to ${outputPath}`);
